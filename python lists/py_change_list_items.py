# change item value
thislist = ["apple", "banana", "cherry"]
thislist[1] = "blackcurrant"
print(thislist)
print("\n")

# change a range of item values
thislist1 = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
thislist1[1:3] = ["balckcurrant", "watermelon"]
print(thislist1)
print("\n")

# insert items
thislist2 = ["apple", "banana", "cherry"]
thislist2.insert(2, "watermelon")
print(thislist2)
print("\n")