# List
thislist = ["apple", "banana", "cherry"]
print(thislist)
print("\n")

# Allow Duplicates
thislist = ["apple", "banana", "cherry", "orange", "banana"]
print(thislist)
print("\n")

# list length
thislist = ["apple", "banana", "cherry"]
print(len(thislist))
print("\n")

# list items - data types
list1 = ["apple", "banana", "cherry"]
list2 = [1, 5, 7, 9, 3]
list3 = [True, False, False]
list4 = ["abc", 34, True, 40, "male"]
print(list1, list2, list3, list4)