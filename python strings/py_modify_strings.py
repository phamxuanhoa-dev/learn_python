# Upper Case
a = "hello, world!"
print(a.upper())
print("\n")

# Lower Case
a = "Hello, World!"
print(a.lower())
print("\n")

# Remove Whitespace
a = " Hello, World! "
print(a.strip()) #returns "Hello, World!"
print("\n")

# Replace String
a = "Hello, World!"
print(a.replace("H", "J"))
print("\n")

# Split String
a = "Hello, World!"
print(a.split(",")) #returns ['Hello', ' World!']
print("\n")